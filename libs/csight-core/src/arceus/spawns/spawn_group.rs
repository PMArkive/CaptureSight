#[cfg(test)]
use no_std_io::Writer;

use super::spawn_pair::SpawnPair;
use no_std_io::{EndianRead, EndianWrite, Reader};

#[derive(Debug, Clone, Copy, Eq, PartialEq, EndianWrite, EndianRead)]
pub struct SpawnGroup {
    data: [u8; Self::DATA_SIZE],
}

impl SpawnGroup {
    pub const DATA_SIZE: usize = 0x440;
    const PAIR_COUNT: usize = 8;

    fn get_spawn_pair(&self, index: usize) -> SpawnPair {
        if index >= Self::PAIR_COUNT {
            return SpawnPair::default();
        }

        self.default_read_le(index * SpawnPair::DATA_SIZE)
    }

    pub fn get_spawn_pair_seeds(&self, index: usize) -> (u64, u64) {
        let pair = self.get_spawn_pair(index);
        (
            pair.get_spawner_0().get_seed(),
            pair.get_spawner_1().get_seed(),
        )
    }

    pub fn get_is_active(&self) -> bool {
        for index in 0..Self::PAIR_COUNT {
            let spawn_pair = self.get_spawn_pair(index);
            if spawn_pair.get_is_active() {
                return true;
            }
        }

        false
    }

    pub fn get_seed(&self) -> u64 {
        self.default_read_le(0x408)
    }

    #[cfg(test)]
    pub fn set_seed(&mut self, seed: u64) {
        // During tests we'll panic if data isn't set up correctly
        self.write_le(0x408, &seed).unwrap();
    }

    #[cfg(test)]
    pub fn set_spawn_pair(&mut self, spawn_pair: &SpawnPair) {
        // During tests we'll panic if data isn't set up correctly
        self.write_le(0, spawn_pair).unwrap();
    }
}

impl Default for SpawnGroup {
    fn default() -> Self {
        Self {
            data: [0; Self::DATA_SIZE],
        }
    }
}

impl Reader for SpawnGroup {
    fn get_slice(&self) -> &[u8] {
        &self.data
    }
}

#[cfg(test)]
impl Writer for SpawnGroup {
    fn get_mut_slice(&mut self) -> &mut [u8] {
        &mut self.data
    }
}

mod c_api {
    use super::*;

    #[no_mangle]
    pub unsafe extern "C" fn free_spawn_group(ptr: *mut SpawnGroup) {
        crate::utils::free!(ptr);
    }

    #[no_mangle]
    pub unsafe extern "C" fn spawn_group_get_seed(ptr: *mut SpawnGroup) -> u64 {
        crate::utils::run_method!(ptr, get_seed)
    }

    #[no_mangle]
    pub unsafe extern "C" fn spawn_group_get_spawn_pair_seeds(
        ptr: *mut SpawnGroup,
        seed0: *mut u64,
        seed1: *mut u64,
        index: usize,
    ) {
        let spawn_group = crate::utils::open_box!(ptr);
        let (read_seed0, read_seed1) = spawn_group.get_spawn_pair_seeds(index);
        *seed0 = read_seed0;
        *seed1 = read_seed1;
    }
}

